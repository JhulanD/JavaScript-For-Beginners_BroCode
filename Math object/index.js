// Math = Built-in object that provides a collection of properties and methods for mathematical constants and functions.

console.log(Math.E); // 2.718281828459045
console.log(Math.PI); // 3.141592653589793

// Math.round() = Returns the value of a number rounded to the nearest integer.
console.log(Math.round(4.6)); // 5

// Math.ceil() = Returns the value of a number rounded up to its nearest integer.
console.log(Math.ceil(4.2)); // 5

// Math.floor() = Returns the value of a number rounded down to its nearest integer.
console.log(Math.floor(4.8)); // 4

// Math.trunc() = Returns the integer part of a number by removing any fractional digits from a number.
console.log(Math.trunc(4.8)); // 4

// Math.random() = Returns a random number between 0 (inclusive), and 1 (exclusive).
console.log(Math.random());

// Math.pow() = Returns the value of a base raised to the power of an exponent.
console.log(Math.pow(2, 4));

// Math.sqrt() = Returns the square root of a number.
console.log(Math.sqrt(16));

// Math.abs() = Returns the absolute value of a number.
console.log(Math.abs(-4));

// Math.min() = Returns the lowest value in a list of arguments.
console.log(Math.min(0, 150, 30, 20, 45, 75));

// Math.max() = Returns the highest value in a list of arguments.
console.log(Math.max(0, 150, 30, 20, 45, 75));

// Math.cbrt() = Returns the cube root of a number.
console.log(Math.cbrt(8));

// Math.hypot() = Returns the length of the hypotenuse of a right triangle.
console.log(Math.hypot(3, 4));

// Math.sin() = Returns the sine of a number.
console.log(Math.sin(90));

// Math.cos() = Returns the cosine of a number.
console.log(Math.cos(90));

// Math.tan() = Returns the tangent of a number.
console.log(Math.tan(90));

// Math.asin() = Returns the arc sine of a number.
console.log(Math.asin(1));

// Math.acos() = Returns the arc cosine of a number.
console.log(Math.acos(0));

// Math.atan() = Returns the arc tangent of a number.
console.log(Math.atan(1));

// Math.sign() = Returns the sign of a number.
console.log(Math.sign(-4));

// Math.cosh() = Returns the hyperbolic cosine of a number.
console.log(Math.cosh(1));