// Arithmetic Operators

// + addition
let x = 10;
let y = 20;
let z = x + y;
console.log(z);
// - subtraction
let a = 10;
let b = 20;
let c = a - b;
console.log(c);
// * multiplication
let d = 10;
let e = 20;
let f = d * e;
console.log(f);
// / division
let g = 10;
let h = 20;
let i = g / h;
console.log(i);
// % modulus
let j = 10;
let k = 20;
let l = j % k;
console.log(l);
// ** exponentiation
let m = 10;
let n = 20;
let o = m ** n;
console.log(o);
// ++ increment
let p = 10;
let q = 20;
let r = p++;
console.log(r);
// -- decrement
let s = 10;
let t = 20;
let u = s--;
